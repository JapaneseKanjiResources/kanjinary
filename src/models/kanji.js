class Kanji {
    constructor(
        ideogram,
        kunyomi,
        onyomi,
        words,
        parts,
        radical,
        strokes,
        lvl
    ) {
        this.ideogram = ideogram
        this.kunyomi = kunyomi
        this.onyomi = onyomi
        this.words = words
        this.parts = parts
        this.radical = radical
        this.strokes = strokes
        this.lvl = lvl
    }
}